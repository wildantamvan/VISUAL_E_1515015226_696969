    private void prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesActionPerformed
        JSlider nilai = jSlider1;
        String inpnama1 = inpnama.getText();
        float inpberat1 = Float.parseFloat(inpberat.getText());
        String inptelp1 = inptelp.getText();
        float totaldiskon;
        float biaya = 0;
        float Diskon = Float.valueOf(jSlider1.getValue());
        String paket;
tfbayar.setEditable(false);
        if (reguler.isSelected()) {
            paket = reguler.getText();
            totaldiskon = (float) (inpberat1 * 11500) * (Diskon / 100);
            biaya = (float) (inpberat1 * 11500) - totaldiskon;
        } else if (ekspress.isSelected()) {
            paket = ekspress.getText();
            totaldiskon = (float) (((inpberat1 * 11500) * 1.5) * Diskon / 100);

            biaya = (float) (((inpberat1 * 11500) * 1.5) - totaldiskon);
        } else {
            JOptionPane.showMessageDialog(null, "tidak bisa diproses, jasa pengiriman belum dipilih", "Warning", JOptionPane.INFORMATION_MESSAGE);
            return;

        }
        tfbayar.setText("Rp. " + biaya);
        output.setText(
                "\n "
                + " Nama\t: " + inpnama1
                + "\n No.HP\t: " + inptelp1
                + "\n Berat\t: " + inpberat1 + " Kg"
                + "\n Diskon\t: " + totaldiskon
                + "\n Jenis Jasa\t: " + paket
                + "\n Total\t: Rp " + biaya
                + "\n");
       
    }//GEN-LAST:event_prosesActionPerformed

