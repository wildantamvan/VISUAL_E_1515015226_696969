    private void inptelpKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inptelpKeyPressed
       inptelp.addKeyListener(new KeyAdapter() {
        public void keyTyped(KeyEvent e){
            char k = e.getKeyChar();
            if(!((k >= '0') && (k <= '9')) || (k == '\b')){
                e.consume();
            }
        }});
    }//GEN-LAST:event_inptelpKeyPressed

